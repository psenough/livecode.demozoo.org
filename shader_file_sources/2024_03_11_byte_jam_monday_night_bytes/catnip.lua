  	rectb(origin.x,origin.y,size.x,size.y,colour)
  else
  	rect(origin.x,origin.y,size.x,size.y,colour)
  end
 elseif type==1 then
  if border then
  	circb(origin.x,origin.y,size.x,colour)
  else
  	circ(origin.x,origin.y,size.x,colour)
  end
 elseif type==2 then
  if border then
	  ellib(origin.x,origin.y,size.x,size.y,colour)
  else
  	elli(origin.x,origin.y,size.x,size.y,colour)
  end
 elseif type==3 then
  if border then
  	trib(
	   origin.x-size.x,origin.y+size.y,
	   origin.x+size.x,origin.y+size.y,
	   origin.x,origin.y-size.y,
	   colour)
  else
	  tri(
	   origin.x-size.x,origin.y+size.y,
	   origin.x+size.x,origin.y+size.y,
	   origin.x,origin.y-size.y,
	   colour)
  end
 elseif type==4 then
  line(origin.x,origin.y,origin.x+size.x,origin.y-size.y,colour)
 elseif type==5 then
  pix(origin.x,origin.y,colour)
 end
end

